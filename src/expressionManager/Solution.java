package expressionManager;

public class Solution {
	private double numericSolution;
	private boolean booleanSolution;
	
	public double getNumericSolution() {
		return numericSolution;
	}
	public void setNumericSolution(double numericSolution) {
		this.numericSolution = numericSolution;
	}
	public boolean isBooleanSolution() {
		return booleanSolution;
	}
	public void setBooleanSolution(boolean booleanSolution) {
		this.booleanSolution = booleanSolution;
	}
}
